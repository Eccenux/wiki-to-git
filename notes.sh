--date=<date> 
ISO 8601 or 
`<unix-timestamp> <time-zone-offset>`, where <unix-timestamp> is the number of seconds since the UNIX epoch. <time-zone-offset> is a positive or negative offset from UTC. For example CET (which is 1 hour ahead of UTC) is +0100.
